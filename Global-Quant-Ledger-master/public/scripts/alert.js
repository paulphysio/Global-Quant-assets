const txnTypes = ["deposit", "withdrawal"];
// get main tag
const main = document.getElementsByTagName("main")[0];
// set postition to relative
main.style.position = "relative";

async function showAlert() {
  // PICK RANDOMA NAME, COUNTRY
  const newTxn = pickRandomVal(txnTypes);
  const newName = pickRandomVal(names);
  const newCountry = pickRandomVal(countries);
  const randomAmount = generateRandomVal(47500, 3000);
  const div = document.createElement("div");
  // Add classname to alert div
  div.className = `withdrawal_alert`;

  // Add Text
  div.appendChild(
    document.createTextNode(
      `${newName} from ${newCountry} has just made a ${newTxn} of $${randomAmount}`
    )
  );

  // append child to main tag
  main.appendChild(div);

  // Remove Element After 3 secs
  setTimeout(function () {
    div.remove();
  }, 5000);
}

// PICK RANDOM VALUE METHOD
function pickRandomVal(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}
// GENERATE RANDOM VALUE METHOD
function generateRandomVal(start, addon) {
  return Math.floor(Math.random() * start + addon);
}
// GENERATE SECONDS
function generateSeconds() {
  let seconds = Math.floor(Math.random() * 65000 + 4000);
  return seconds;
}

setInterval(showAlert, generateSeconds());
